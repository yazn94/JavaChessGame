package org.example.generalUse;


/**
 * This class provides a collection of ANSI codes
 * that can be used to modify text decoration and color in the console.
 */


public class ANSI {
    public static final String WHITE_BOLD = "\u001B[37m\u001B[1m";
    public static final String BLACK_BOLD = "\u001B[30m\u001B[1m";
    public static final String UNDER_LINED = "\u001B[4m";
    public static final String RED = "\u001B[31m";
    public static final String GREEN = "\u001B[32m";
    public static final String BLUE = "\u001B[34m";
    public static final String RESET = "\u001B[m";
}
